    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        $validator = Validator($request->all(),[
            'quiz_name' => 'required|string',
            
            
        ]);

        if(!$validator->fails())
        {
            $quiz = new Quiz();
            $quiz->quiz_name = $request->quiz_name;
            $quiz->course_id = $request->quiz_course_id;
            $quiz->section_id = $request->quiz_section_id;
            $isSaved = $quiz->save();

            return response()->json([
                'message' => $isSaved ? "Quiz Created Successfully" : "Failed to Create Quiz"
            ], $isSaved ? Response::HTTP_CREATED : Response::HTTP_BAD_REQUEST);

            } else {
            //VALIDATION FAILED في حال فشل الفاليديشن
            return response()->json([
                'message' => $validator->getMessageBag()->first()
            ], Response::HTTP_BAD_REQUEST);
        }
    }

